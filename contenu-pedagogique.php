				<h1>Une Ecole de Théâtre : La Masterclass sous la direction de </br>Lidia Alekséevna Monakova & Fabrice Carrey </h1>
				<h2>En quoi consiste le projet ?</h2>
				<p>Le projet consiste à proposer une formation artistique pluridisciplinaire centrée sur l'art de l'acteur : une immersion totale afin de découvrir l'école théâtrale russe... comme si vous étiez un élève russe ou biélorusse. </p>
				<p>Au-delà de la formation artistique, c'est aussi, pour les comédiens, l'occasion de découvrir un pays, une culture, et surtout une autre approche du Théâtre Russe, comme expérience humaine d'une vie commune basée sur le partage et la solidarité, avec des valeurs d'exigence, de rigueur et de persévérance dans le travail.</p>
				<h2>Organisation et déroulement de la formation</h2>
				<p>La formation se déroule en deux phases successives. Dans une première phase, sont proposés aux participants des travaux sur la notion : « De l’Acte au Sentiment » : une mobilisation vraie et des actes justes font naturellement naître, pendant le processus du jeu, des sentiments et des sensations sincères chez l’acteur. La méthode de travail est basée, d’une part, sur la recherche des procédés qui doivent aider l’acteur, dans son jeu, à éprouver des sentiments et des sensations vrais et, d’autre part, sur l’analyse du processus «psycho-physiologique» qui se produit sur scène. 
				À cet effet, différents modules et exercices sont proposés au comédien stagiaire afin de lui permettre de mettre en pratique toutes les étapes du processus de construction d’un personnage.
				</p>
				
				<h2>Une formation pluridisciplinaire</h2>
				
				<p>Bien qu’essentiellement centrée sur le théâtre, cette formation artistique est pluridisciplinaire, et intègre diverses disciplines. Sont enseignés : le chant, la danse, le combat et l’escrime, la plastique, le travail sur le tempo et le rythme, etc… 
				En effet, l’art de l’acteur, tel qu’il se conçoit dans « l’École Théâtrale Russe », fait appel non seulement à l’imagination, mais aussi au corps et à la voix du comédien, comme des instruments que ce dernier doit savoir maîtriser. C’est pourquoi la formation de l’acteur passe également par l’apprentissage de telles disciplines : celles-ci sont abordées non pas comme des « spécialités », mais comme des « outils » au service de l’acteur.</p>
				
				<h2>Les Objectifs</h2>
				<p>Après la découverte de l’École Théâtrale Russe, l'objectif est de permettre à chaque participant d’acquérir de solides bases et techniques du comédien pour construire ses « outils » d’acteur. 
				Bien que la compréhension de la langue russe ne soit pas requise, il est possible d’étudier la culture russe et européenne et, pour les plus motivés, d’apprendre le Russe (car les 1800 heures de cours sont traduites simultanément du russe vers le français). 
				Eloigné de son pays d’origine, chaque participant aura ainsi l'opportunité de découvrir un nouvel art de vivre.
				Nous souhaitons réunir des citoyens responsables, chacun avec son parcours personnel, afin d’enrichir un projet artistique ambitieux. (En 2012 et 2013 : Voyage à Moscou et participation du groupe au Festival de chanson théâtrale de Perm).</p>
