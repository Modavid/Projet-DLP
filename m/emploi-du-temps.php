			<h1>Emploi du temps de la Masterclass</h1>
			<p>Les cours ont lieu du lundi au samedi, à raison de 8 heures 30 par jour, le dimanche étant réservé au repos ou aux répétitions éventuelles. Les cours sont dispensés par des professeurs russes et traduits simultanément en français, dans les disciplines suivantes : Technique du comédien, Acrobatie / Plastique Chant individuel et collectif, Combat / Escrime / Jonglage, Tempo-rythme, Biomécanique, Diction, Danse classique, Piano, Claquettes, Maquillage.</p>
			<p>Une journée de cours est organisée, généralement, de la manière suivante : </br>
			- Le matin : travail, sous forme d’exercices, sur la disponibilité du corps, la concentration, l’attention et l’écoute, l’éveil des sens, l’imagination, la notion de l’individu et du groupe, la mémoire sensorielle, la précision du geste. Travaux sur le sens du tempo et du rythme (cadence du mouvement et de l’intensité émotionnelle), la coordination dans l’espace (équilibre, impulsion, centre de gravité, précision du mouvement).</br>
			- L’après-midi : mise en pratique des exercices du matin et analyse du jeu (logique de l’action). Training de Techniques du comédien, improvisations et travail d’études et d’observations.
			</p>
			
