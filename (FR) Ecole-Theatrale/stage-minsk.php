				<h1 style="margin-top: 30px"> Stage pluridisciplinaire en immersion intensive  au  RTBD <br/>( Théâtre Républicain de la Dramaturgie Biélorusse )</h1>
		        <p style="margin-bottom: 30px">Le stage propose une découverte de l’école théâtrale Russe telle qu’elle est enseignée en Biélorussie, selon les principes et les méthodes de Stanislavsky, Michael Chekhov, Meyerhold et Tovstonogov.</p>
				<p style="margin-bottom: 30px">Tous les cours sont dispensés par des professeurs formés à l’Académie Nationale Biélorusse des Arts de Minsk. Afin d’enrichir cette rencontre artistique avec l’école théâtrale russe, trois masterclass sont également prévues avec des artistes associés Biélorusses pendant la période de stage.</p>
				<p style="margin-bottom: 30px">Notre objectif est d’offrir aux stagiaires une formation intensive et multidisciplinaire. Bien qu’essentiellement centrée sur les techniques du comédien, cette formation intègre également diverses disciplines nécessaires à la formation du comédien. En effet, l’art de l’acteur, tel qu’il se conçoit dans « l’École Théâtrale Russe », fait appel non seulement à l’imagination, mais aussi au corps et à la voix du comédien, comme des instruments que ce dernier doit savoir maîtriser pour exprimer avec précision ses émotions. Ces disciplines sont abordées non pas comme des « spécialités », mais comme des « outils » au service de l’acteur pour l’aider à développer et à aiguiser son art.</p>
				<h1 style="margin-bottom: 30px">Les disciplines enseignées</h1>
				<p style="margin-bottom: 30px">Techniques du comédien, biomécanique, diction, danse classique et traditionnelle, chant individuel et collectif, combat/plastique/escrime, acrobatie, tempo-rythme, russe.</p>
				<h1 style="margin-bottom: 30px"><img src="Planning août 2015.jpg" alt="planning" style="width:428px;height:390px"></h1>
				 <h1 style="margin-bottom: 30px">Déroulement</h1>
				<p style="margin-bottom: 30px">Les cours s’articulent autour d’une notion : « De l’Acte au Sentiment ». Une mobilisation vraie et des actes justes font naturellement naître, pendant le processus du jeu, des sentiments et des sensations sincères. Nous appliquons les méthodes basées sur la recherche de procédés vrais destinés à aider le stagiaire à éprouver des sentiments (sensations) lors du jeu et à analyser les processus «psycho-physiologiques» qui se déroulent sur scène.Plusieurs modules sont proposés pour permettre au comédien de mettre en pratique toute l’évolution des étapes dans la construction du personnage. L’évolution du travail s’articule de la manière suivante : </p>
				<ul>
				<li class="item-lm" style="margin-bottom: 30px"><B>Le matin :</B> travail sur la disponibilité du corps, la concentration, l’attention et l’écoute, l’éveil des sens, l’imagination, la notion de l’individu et du groupe, la mémoire sensorielle, la précision du geste sous forme d’exercices. </li> 
				
				<li class="item-lm" style="margin-bottom: 30px"><B>L’après-midi :</B> mise en pratique des exercices du matin, avec des travaux sur le sens du tempo et du rythme (cadence du mouvement et de l’intensité émotionnelle), la coordination dans l’espace (équilibre, impulsion, centre de gravité, précision du mouvement), et l’analyse du jeu (logique de l’action). Le tout ponctué par des improvisations et la présentation de travaux à base d’exercices personnels</li>
			</ul>
			
